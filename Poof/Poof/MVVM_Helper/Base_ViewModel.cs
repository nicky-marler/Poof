namespace Poof.MVVM_Helper
{
    public class Base_ViewModel : Observable_Object
    {
        bool isBusy;
        bool isNotBusy = true;

        public bool IsBusy
        {
            get => isBusy;
            set
            {
                if (SetProperty(ref isBusy, value))
                    IsNotBusy = !isBusy;
            }
        }

        public bool IsNotBusy
        {
            get => isNotBusy;
            set
            {
                if (SetProperty(ref isNotBusy, value))
                    IsBusy = !isNotBusy;
            }
        }
    }
}
